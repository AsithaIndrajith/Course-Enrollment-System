    void loadTimeAllocations(){
        try {
            String lecid=lec.getLecturerID();
            ArrayList<Time> l = searchTeachingAllocationByLecturerID(lecid);
             DefaultTableModel dtm = (DefaultTableModel) times.getModel();
            dtm.setRowCount(0);
            for (Time lo : l) {
                Object[] rowData = {lo.getLecID(),lo.getYr(),lo.getSem(),lo.getSubID(),lo.getLocID(),lo.getInsID()};
                dtm.addRow(rowData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdministratorInstructorUpdate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratorInstructorUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
