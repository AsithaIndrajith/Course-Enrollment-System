    private void signInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInActionPerformed
        try {
            // TODO add your handling code here:
            depid=searchDepartmentByDepartmentName((String)department.getSelectedItem()).getDepartmentID();
           
            if(((lec=searchLecturerByLecturerID(username.getText()))!=null)&&lec.getLecturerPassword().equals(password.getText())&&lec.getDepID().equals(depid)){
                JOptionPane.showMessageDialog(rootPane, "Sign In Success");
                this.setVisible(false);
                lecHome=new LecturerHome();
                lecHome.setVisible(true);
                lecHome.setLocationRelativeTo(null);
            }
           
            else{
                JOptionPane.showMessageDialog(rootPane, "Sign In Failed");
            }
                
        } catch (SQLException ex) {
            Logger.getLogger(LecturerLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(LecturerLogin.class.getName()).log(Level.SEVERE, null, ex);
        }catch(NullPointerException n){
            JOptionPane.showMessageDialog(rootPane, "Invalid Login!!!");
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Try again later!!!");
        }
        
    }//GEN-LAST:event_signInActionPerformed

