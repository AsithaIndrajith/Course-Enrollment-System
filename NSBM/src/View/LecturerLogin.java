    private void signInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInActionPerformed
        try {
            // TODO add your handling code here:
            lec=searchLecturer(username.getText());
            if((lec!=null)&&lec.getLecturerPassword().equals(password.getText())){
                JOptionPane.showMessageDialog(rootPane, "Sign In Success");
                this.setVisible(false);
                lecHome=new LecturerHome();
                lecHome.setVisible(true);
                lecHome.setLocationRelativeTo(null);
            }
            if(!lec.getDepID().equals(depid)){
                JOptionPane.showMessageDialog(rootPane, "You cannot access to "+depid+" department");
                System.exit(0);
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Sign In Failed");
            }
                
        } catch (SQLException ex) {
            Logger.getLogger(LecturerLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(LecturerLogin.class.getName()).log(Level.SEVERE, null, ex);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Something Went Wrong");
        }
        
    }//GEN-LAST:event_signInActionPerformed

