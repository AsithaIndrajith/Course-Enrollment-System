    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        this.setVisible(false);
        posReg=new PostgraduateRegister();
        posReg.setVisible(true);
        ArrayList<Degree>dg=new ArrayList<Degree>();
        ArrayList<String>s=new ArrayList<>();
        try {
            dg=searchDegree(depid);
            if(dg!=null){
                String ls[]=new String[dg.size()];
                for(Degree d:dg){
                    s.add(d.getDegreeName());
                }
                ls=s.toArray(ls);
                posReg.getEduCol().setModel(new DefaultComboBoxModel<>(ls));
            }
        } catch (SQLException ex) {
            Logger.getLogger(PostgraduateLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PostgraduateLogin.class.getName()).log(Level.SEVERE, null, ex);
        }catch(Exception e){
            JOptionPane.showMessageDialog(jPanel1, "Try again later");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

