    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        CS=new Course();
        String id=csID.getText();
        String name=csName.getText();
        String deg=csDeg.getText();
        String dep=depid;
        String type=((String)typ.getSelectedItem()).equals("BSc")?"U":"P";
        CS.setCourseID(id);
        CS.setName(name);
        CS.setDegreeID(deg);
        CS.setDepartmentID(dep);
        CS.setType(type);
        Degree d=new Degree();
        d.setDegreeID(deg);
        d.setDepID(depid);
        d.setDegreeName(type+name);
        try {
            if(insertDegree(d)>0){
                int res=insertCourse(CS);
                if(res>0){
                    JOptionPane.showMessageDialog(rootPane, "Added successfull");

                }
                else{
                    JOptionPane.showMessageDialog(rootPane, "Try again later");
                }
            }
           

        } catch (SQLException ex) {
            Logger.getLogger(UndergraduateRegister.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UndergraduateRegister.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addBtnActionPerformed

