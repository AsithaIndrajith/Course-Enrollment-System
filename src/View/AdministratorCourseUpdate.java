    void load(){
        try {
            ArrayList<Course>l;
            l = searchCoursesByDepartmentID(depid);
            DefaultTableModel dtm = (DefaultTableModel) course.getModel();
            dtm.setRowCount(0);
            for (Course lo : l) {
                Object[] rowData = {lo.getCourseID(),lo.getName(),lo.getDegreeID()};
                dtm.addRow(rowData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdministratorCourseUpdate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratorCourseUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void findCourseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findCourseBtnActionPerformed
        // TODO add your handling code here:
        String id=courseIDTxt.getText();
        try {
            CS=searchCourseByCourseID(id);
            if(CS==null){
                JOptionPane.showMessageDialog(rootPane, "Course record does not exist!!!");
            }
            else{
               
                courseNameTxt.setText(CS.getName());
                degreeTxt.setText(CS.getDegreeID());
                degreeTypeComboBox.setSelectedIndex(CS.getType().equals("U")?0:1);
                
        }
        } catch (SQLException ex) {
            Logger.getLogger(AdministratorUndergraduateUpdate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratorUndergraduateUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }catch(Exception e){
            System.out.println(e);
            JOptionPane.showMessageDialog(rootPane,"Try again later");
        }
    }//GEN-LAST:event_findCourseBtnActionPerformed

    private void addCourseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCourseBtnActionPerformed
        // TODO add your handling code here:
        CS=new Course();
        String id=courseIDTxt.getText();
        String name=courseNameTxt.getText();
        String deg=degreeTxt.getText();
        String dep=depid;
        String type=((String)degreeTypeComboBox.getSelectedItem()).equals("BSc")?"U":"P";
        CS.setCourseID(id);
        CS.setName(name);
        CS.setDegreeID(deg);
        CS.setDepartmentID(dep);
        CS.setType(type);
        Degree d=new Degree();
        d.setDegreeID(deg);
        d.setDepID(depid);
        d.setDegreeName(type+name);
        try {
            if(insertDegree(d)>0){
               
                if(insertCourse(CS)>0){
                    JOptionPane.showMessageDialog(rootPane, "Added successfull!!!");
                    this.load();

                }
                else{
                    JOptionPane.showMessageDialog(rootPane, "Try again later");
                }
            }
           

        } catch (SQLException ex) {
            Logger.getLogger(UndergraduateRegister.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UndergraduateRegister.class.getName()).log(Level.SEVERE, null, ex);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Try again later!!!");
        }
    }//GEN-LAST:event_addCourseBtnActionPerformed

