    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            //Delete allocated time for the lecturer
            String lecid=(String) lecturerComboBox.getSelectedItem();
            String year=(String) yearComboBox.getSelectedItem();
            String semester=(String) semesterComboBox.getSelectedItem();
            
            Time t=new Time();
            t.setLecID(lecid);
            t.setYr(year);
            t.setSem(semester);
            
            deleteTimeAllocation(t);
            this.loadTimeAllocations();
            JOptionPane.showMessageDialog(rootPane, "Deleted Successfully!!!");
            
            
        } catch (SQLException ex) {
            Logger.getLogger(AdministratorTimeAllocation.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "Instructor cannot be deleted.!!!");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratorTimeAllocation.class.getName()).log(Level.SEVERE, null, ex);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Try again later!!!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

