    void loadLecturers(){
        try {
            ArrayList<String>lecturerIDs=new ArrayList<>();
            
            for(Lecturer l:searchLecturerByDepartmentID(EnrollementSystemHome.depid)){
                lecturerIDs.add(l.getLecturerID());
            }
            String[] slist=new String[lecturerIDs.size()];
            slist=lecturerIDs.toArray(slist);
            lecturerComboBox.setModel(new DefaultComboBoxModel<>(slist));
        } catch (SQLException ex) {
            Logger.getLogger(AdministratorTimeAllocation.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratorTimeAllocation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    void loadTimeAllocations(){
        
        try {
            String lecid=(String) lecturerComboBox.getSelectedItem();
            ArrayList<Time> l = searchTeachingAllocationByLecturerID(lecid);
             DefaultTableModel dtm = (DefaultTableModel) times.getModel();
            dtm.setRowCount(0);
            for (Time lo : l) {
                Object[] rowData = {lo.getLecID(),lo.getYr(),lo.getSem(),lo.getSubID(),lo.getLocID(),lo.getInsID()};
                dtm.addRow(rowData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdministratorInstructorUpdate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratorInstructorUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
