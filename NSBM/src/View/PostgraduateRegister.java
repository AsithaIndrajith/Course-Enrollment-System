    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        String name=nameTxt.getText();
        String nic=NICTxt.getText();
        String dob=(String)year.getSelectedItem()+"-"+(String)month.getSelectedItem()+"-"+(String)date.getSelectedItem();
        String address=addressTxt.getText();
        String telephone=telephoneTxt.getText();
        String edu=(String)getEduCol().getSelectedItem();
        String institut=(String)institute.getSelectedItem();
        String faculty=depid;
        String sequrity=(String)secQ.getSelectedItem();
        String answer=(String)answerTxt.getText();
        pos=new Postgraduate();
        pos.setName(name);
        pos.setNic(nic);
        pos.setDob(dob);
        pos.setAddress(address);
        pos.setTelephone(telephone);
        pos.setInstitute(institut);
        pos.setQualifications(edu);
        pos.setDepartment(faculty);
        pos.setSecurity(sequrity);
        pos.setAnswer(answer);
        try {
            if(insertPostgraduate(pos)>0){
                JOptionPane.showMessageDialog(rootPane, "You have succefully registered!!!");
                pos.setId(searchPostgraduateID(pos.getNic()));
                this.setVisible(false);
                posCs=new PostgraduateCourseRegister();
                posCs.setVisible(true);
                posCs.setLocationRelativeTo(null);
                ArrayList<Course>c=new ArrayList<Course>();
                c=searchCourseIDD(depid,"P");
                if(c!=null){
                    ArrayList<String>s=new ArrayList<>();
                    String l[]=new String[c.size()];
                    for(Course d: c){
                        s.add(d.getName());
                    }
                    l=s.toArray(l);
                    posCs.getSelectCourse().setModel(new DefaultComboBoxModel<>(l));
                    JOptionPane.showMessageDialog(rootPane, "Your personal details collected.\n"+"Your ID: "+pos.getId());
                    
                   
                    if(((Integer.parseInt(getTime().substring(0,2))-1)*30+Integer.parseInt(getTime().substring(3,5)))>167&&((Integer.parseInt(getTime().substring(0,2))-1)*30+Integer.parseInt(getTime().substring(3,5)))<182){
                        posCs.getSelectSemester().setSelectedIndex(2);
                        posCs.getSelectSemester().setEditable(false);
                        posCs.getSelectSemester().setEnabled(false);
                    }
                }
                else{
                    JOptionPane.showMessageDialog(rootPane,"There are no courses avaiable for you.");
                }
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(PostgraduateRegister.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PostgraduateRegister.class.getName()).log(Level.SEVERE, null, ex);
        }catch(Exception e){
            System.out.println(e);
            JOptionPane.showMessageDialog(rootPane,"Try again later.");
        }
    
    }//GEN-LAST:event_submitActionPerformed

