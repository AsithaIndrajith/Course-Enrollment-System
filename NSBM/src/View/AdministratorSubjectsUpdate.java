    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        String id=subId.getText();
        String name=subName.getText();
        String sem=subSem.getText();
        String yr=subYear.getText();
        String credits=subCredit.getText();
        String price=subPrice.getText();
        sub=new Subject();
        sub.setSubjectID(id);
        sub.setSubjectName(name);
        sub.setSubjectSemester(sem);
        sub.setSubjectYear(yr);
        sub.setCredits(credits);
        sub.setPrice(price);
        try {
            int res=updateSubject(sub);
            if(res>0){
                JOptionPane.showMessageDialog(rootPane, "Update successfull!!!");

            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Your details are wrong.");
            }
            

        } catch (SQLException ex) {
            Logger.getLogger(UndergraduateRegister.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UndergraduateRegister.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateBtnActionPerformed

