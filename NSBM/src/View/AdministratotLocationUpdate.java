    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            // TODO add your handling code here:
            ArrayList<Location>l= searchLoc(depid);
            DefaultTableModel dtm = (DefaultTableModel) location.getModel();
            dtm.setRowCount(0);
            for (Location lo : l) {
                Object[] rowData = {lo.getLocationID(),lo.getLocationName()};
                dtm.addRow(rowData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdministratotLocationUpdate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratotLocationUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

