    //load all lecturers
    void loadLecturers(){
        try {
            ArrayList<Lecturer> lecturer = searchLecturerByDepartmentID(depid);
            
            DefaultTableModel dtm = (DefaultTableModel) lecturers.getModel();
            dtm.setRowCount(0);
            
            for (Lecturer l : lecturer) {
                Object[] rowData = {l.getLecturerID(),l.getLecturerName(),l.getLecturerAddress(),l.getLecturerPhone()};
                dtm.addRow(rowData);
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(AdministratorInstructorUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void findActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findActionPerformed
        // search lecturer for editing
        String id=lecID.getText();
        try {
            lec=searchLecturerByLecturerID(id);
            if(lec==null){
                JOptionPane.showMessageDialog(rootPane, "Lecturer record does not exist!!!");
                
                nameTxt.setText("");
                addressTxt.setText("");
                nicTxt.setText("");
                telephoneTxt.setText("");
                pwdTxt.setText("");
            }
            else{
                nameTxt.setText(lec.getLecturerName());
                telephoneTxt.setText(lec.getLecturerPhone());
                addressTxt.setText(lec.getLecturerAddress());
                nicTxt.setText(lec.getLecturerNIC());
                pwdTxt.setText(lec.getLecturerPassword());

            }
        } catch (SQLException ex) {
            Logger.getLogger(AdministratorUndergraduateUpdate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratorUndergraduateUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Try again later!!!");
        }
    }//GEN-LAST:event_findActionPerformed

