    private void refreshBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshBtnActionPerformed
        // TODO add your handling code here:
        String id=csID.getText();
        try {
            CS=searchCourse(id);
            if(CS==null){
                JOptionPane.showMessageDialog(rootPane, "Course record does not exist!!!");
            }
            else{
                csName.setText(CS.getName());
                csDeg.setText(CS.getDegreeID());
                typ.setSelectedIndex(CS.getType().equals("U")?0:1);
                ArrayList<Course>l;
                l = searchCourses(depid);
                DefaultTableModel dtm = (DefaultTableModel) course.getModel();
                dtm.setRowCount(0);
                for (Course lo : l) {
                    Object[] rowData = {lo.getCourseID(),lo.getName(),lo.getDegreeID()};
                    dtm.addRow(rowData);
            }
        }
        } catch (SQLException ex) {
            Logger.getLogger(AdministratorUndergraduateUpdate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratorUndergraduateUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }catch(Exception e){
            System.out.println(e);
            JOptionPane.showMessageDialog(rootPane,"Try again later");
        }
    }//GEN-LAST:event_refreshBtnActionPerformed

