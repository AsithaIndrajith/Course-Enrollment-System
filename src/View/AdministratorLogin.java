    private void signInBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInBtnActionPerformed
        try {                                       
        EnrollementSystemHome.depid=searchDepartmentByDepartmentName((String)selectDepartmentComboBox.getSelectedItem()).getDepartmentID();
            
        String id = usernameTxt.getText();
        String pswd=passwordTxt.getText();
                
        if(searchAdministrator(id, pswd)>0){
            JOptionPane.showMessageDialog(jPanel1, "Sign In Success");
            this.setVisible(false);
            administratorHome=new AdministratorHome();
            administratorHome.setVisible(true);
            administratorHome.setLocationRelativeTo(null);
        }
        else{
            JOptionPane.showMessageDialog(jPanel1, "Sign In Failed");
        }
        
        } catch (SQLException ex) {
            Logger.getLogger(AdministratorLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratorLogin.class.getName()).log(Level.SEVERE, null, ex);
        }catch(NullPointerException n){
            JOptionPane.showMessageDialog(rootPane, "Invalid Login!!!");
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Try again later!!!");
        }
            
    }//GEN-LAST:event_signInBtnActionPerformed

