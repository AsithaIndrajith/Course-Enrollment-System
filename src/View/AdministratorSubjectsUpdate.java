    void loadSubjectTable(){
        
        try {
            ArrayList<Subject> l = searchSubjectByDepartmentID(depid);
            DefaultTableModel dtm = (DefaultTableModel) subjectss.getModel();
            dtm.setRowCount(0);
            for (Subject lo : l) {
                Object[] rowData = {lo.getType().equals("P")?"Postgraduate":"Undergraduate",lo.getCourseID(),lo.getSubjectID(),lo.getSubjectName(),lo.getSubjectYear(),lo.getSubjectSemester(),lo.getCredits(),lo.getPrice(),lo.getLecturerID()};
                dtm.addRow(rowData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdministratorInstructorUpdate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratorInstructorUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
