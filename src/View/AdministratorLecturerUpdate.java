    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // add new lecturer
        lec=new Lecturer();
        
        String id=lecID.getText();
        String name=nameTxt.getText();
        String nic=nicTxt.getText();
        String phone=telephoneTxt.getText();
        String address=addressTxt.getText();
        String pwd=pwdTxt.getText();
        
        lec.setLecturerID(id);
        lec.setLecturerName(name);
        lec.setLecturerNIC(nic);
        lec.setLecturerPhone(phone);
        lec.setLecturerAddress(address);
        lec.setLecturerPassword(pwd);
        lec.setDepID(depid);
        try {
            
            if(insertLecturer(lec)>0){
                JOptionPane.showMessageDialog(rootPane, "Added successfull!!!");
                this.loadLecturers();

            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Try again later");
            }

        } catch (SQLException ex) {
            Logger.getLogger(UndergraduateRegister.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UndergraduateRegister.class.getName()).log(Level.SEVERE, null, ex);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Try again later!!!");
        }
    }//GEN-LAST:event_addBtnActionPerformed

