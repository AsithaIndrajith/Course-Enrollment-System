    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // update postgraduate students
        
        String name=nameTxt.getText();
        String nic=NICTxt.getText();
        String dob=dobTxt.getText();
        String address=addressTxt.getText();
        String telephone=telephoneTxt.getText();
        String edu=eduTxt.getText();
        String institut=instituteTxt.getText();
        String pwd=pswdTxt.getText();
        
        posG.setName(name);
        posG.setNic(nic);
        posG.setDob(dob);
        posG.setAddress(address);
        posG.setTelephone(telephone);
        posG.setInstitute(institut);
        posG.setQualifications(edu);
        posG.setPassword(pwd);
        
        try {
            if(updatePostgraduate(posG)>0){
                JOptionPane.showMessageDialog(jPanel1, "Updated successfully!!!");
                this.loadPostgraduates();
            }
            else{
                JOptionPane.showMessageDialog(jPanel1, "Something went wrong!!!");
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(PostgraduateRegister.class.getName()).log(Level.SEVERE, null, ex);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Try again later!!!");
        }

    }//GEN-LAST:event_submitActionPerformed

    private void refreshBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshBtnActionPerformed
        //search posthraduate
        String id=idTxt.getText();
        try {
            posG=searchPostgraduateByPostgraduateID(id);
            if(posG==null){
                JOptionPane.showMessageDialog(rootPane, "Student record does not exist!!!");
            }
            else{
                nameTxt.setText(posG.getName());
                telephoneTxt.setText(posG.getTelephone());
                addressTxt.setText(posG.getAddress());
                NICTxt.setText(posG.getNic());
                dobTxt.setText(posG.getDob());
                eduTxt.setText(posG.getQualifications());
                instituteTxt.setText(posG.getInstitute());
                pswdTxt.setText(posG.getPassword());
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(AdministratorUndergraduateUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Try again later!!!");
        }
    }//GEN-LAST:event_refreshBtnActionPerformed

