    private void signInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInActionPerformed
        // TODO add your handling code here:
        try{
            String id = username.getText();
            String pswd=password.getText();
            String department=(String)hm.getSelectDepartment().getSelectedItem();
            dep=new Department();
            dep=searchDepartment(department);
            pos=new Postgraduate();
            pos=searchPostgraduate(id);
            if(pos.getId().equals(id)&&pos.getPassword().equals(pswd)&&pos.getDepartment().equals(dep.getDepartmentID())){
                JOptionPane.showMessageDialog(jPanel1, "Sign In Success");
                this.setVisible(false);
                posHome=new PostgraduateHome();
                posHome.setVisible(true);
                posHome.setLocationRelativeTo(null);
                  
            }
            else{
                JOptionPane.showMessageDialog(jPanel1, "Sign In Failed");
            }
        }catch(Exception e){
            System.out.println(e);
            this.setVisible(false);
            Home hm=new Home();
            hm.setVisible(true);
            hm.setLocationRelativeTo(null);
            
        }
    }//GEN-LAST:event_signInActionPerformed

