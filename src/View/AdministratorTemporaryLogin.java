    private void signInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signInActionPerformed
        try {
            EnrollementSystemHome.depid=searchDepartmentByDepartmentName((String)department.getSelectedItem()).getDepartmentID();

            String id = username.getText();
            String pswd=password.getText();

            if(searchAdministrator(id, pswd)>0){
                JOptionPane.showMessageDialog(rootPane, "Administrator access granted");
                this.dispose();
                cal=new CalendarAdjustDateAndTime();
                cal.setVisible(true);
                cal.setLocationRelativeTo(null);
                  
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Invalid login");
            }

        } catch (SQLException ex) {
            Logger.getLogger(AdministratorLogin.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratorLogin.class.getName()).log(Level.SEVERE, null, ex);
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Try again later!!!");
        }

    }//GEN-LAST:event_signInActionPerformed

