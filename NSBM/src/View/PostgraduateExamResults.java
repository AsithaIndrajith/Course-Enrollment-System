    private void refreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshActionPerformed
        id.setText(pos.getId());
        String stud=(String)id.getText();

        try {
            sb=searchStudentSubject(stud);
            if(sb!=null){
                String sblist[]=new String[sb.size()];
                for(int i=0;i<sb.size();i++){
                    sblist[i]=(String)sb.get(i).getSubjectID();
                    System.out.println(sblist[i]);
                }
                sub.setModel(new DefaultComboBoxModel<>(sblist));
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "No record exists");
            }
           
        } catch (SQLException ex) {
            Logger.getLogger(AdministratorExamUpdate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratorExamUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }catch(NullPointerException n){
            JOptionPane.showMessageDialog(rootPane, "No record exists");
        }
    }//GEN-LAST:event_refreshActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String subid=(String)sub.getSelectedItem();
        String di=pos.getId();
        ex=new ExamGrades();
        try {
            ex=searchExamGrade(di,subid);
            if(ex!=null){
                rsult.setText(ex.getResult());
                grade.setText(ex.getGrade());
                if(Integer.parseInt(rsult.getText())>=50){
                    sbjct=searchSubject(subid);
                    credit.setText(sbjct.getCredits());
                }
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "No record exists");
            }

        } catch (SQLException ex) {
            Logger.getLogger(AdministratorExamUpdate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratorExamUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }catch(Exception n){
            System.out.println(n);
            JOptionPane.showMessageDialog(rootPane, "Try again later");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

