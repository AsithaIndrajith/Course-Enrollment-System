    void loadPostgraduates(){//load all postgraduates in to table
        try {
            ArrayList<Postgraduate> postgraduate = searchPostgraduateDepartmentID(depid);
            
            DefaultTableModel dtm = (DefaultTableModel) postgraduates.getModel();
            dtm.setRowCount(0);
            
            for (Postgraduate p : postgraduate) {
                Object[] rowData = {p.getId(),p.getName(),p.getNic(),p.getAddress(),p.getDob(),p.getInstitute(),p.getQualifications(),p.getTelephone()};
                dtm.addRow(rowData);
            }
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(AdministratorInstructorUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
