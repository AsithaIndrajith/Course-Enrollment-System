    private void findActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findActionPerformed
        // TODO add your handling code here:
        String id=lecID.getText();
        try {
            lec=searchLecturer(id);
            if(lec==null){
                JOptionPane.showMessageDialog(rootPane, "Lecturer record does not exist!!!");
                
                nameTxt.setText("");
                addressTxt.setText("");
                nicTxt.setText("");
                telephoneTxt.setText("");
                pwdTxt.setText("");
            }
            else{
                nameTxt.setText(lec.getLecturerName());
                telephoneTxt.setText(lec.getLecturerPhone());
                addressTxt.setText(lec.getLecturerAddress());
                nicTxt.setText(lec.getLecturerNIC());
                pwdTxt.setText(lec.getLecturerPassword());

            }
        } catch (SQLException ex) {
            Logger.getLogger(AdministratorUndergraduateUpdate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratorUndergraduateUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_findActionPerformed

