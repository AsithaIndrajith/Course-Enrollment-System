    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String locID=loc.getText();
        try {
            Location l=searchLocID(locID);
            if(l!=null){
                name.setText(l.getLocationName());
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "No record exists");
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(AdministratotLocationUpdate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratotLocationUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Location l=new Location();
        l.setDepId(depid);
        l.setLocationID(loc.getText());
        l.setLocationName(name.getText());
        try {
            if(insertLoc(l)>0){
                JOptionPane.showMessageDialog(rootPane, "Added successfull");
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Try again later");
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdministratotLocationUpdate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratotLocationUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

