    private void refreshBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshBtnActionPerformed
        // TODO add your handling code here:
        String id=subId.getText();
        try {
            sub=searchSubject(id);
            if(sub==null){
                JOptionPane.showMessageDialog(rootPane, "Subject record does not exist!!!");
            }
            else{
                subName.setText(sub.getSubjectName());
                subSem.setText(sub.getSubjectSemester());
                subYear.setText(sub.getSubjectYear());
                subPrice.setText(sub.getPrice());
                subCredit.setText(sub.getCredits());
                subCourseID.setText(sub.getCourseID());
                subLecID.setText(sub.getLecturerID());
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(AdministratorUndergraduateUpdate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AdministratorUndergraduateUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_refreshBtnActionPerformed

